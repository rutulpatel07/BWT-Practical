// Declare an integer variable using var
var myInteger = 42;

// Declare a floating-point number variable using let
let myFloat = 3.14;

// Declare a character variable using const
const myChar = 'A';

// Declare a string variable using let
let myString = "Hello, World!";

// Display the values on the screen
console.log("Integer:", myInteger);
console.log("Floating-point number:", myFloat);
console.log("Character:", myChar);
console.log("String:", myString);